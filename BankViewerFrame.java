    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (!validateOfField(qtext, "Question Text")) {
            return;
        }
        
        if (!validateOfField(idtext, "Question ID")) {
            return;
        }
        
        if (!validateOfField(gradestext, "Question Possible Grade")) {
            return;
        }
        
        if (!validationOfNumber(gradestext, "Question Possible Grade")) {
            
            return;
            
        }
        
        if (!(mcQ.isSelected() || tf.isSelected() || fb.isSelected())) {
            
            JOptionPane.showMessageDialog(this, "Question type field is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!validateOfField(answerText, "Question Correct Answer")) {
            return;
        }
        
        if (mcQ.isSelected()) {
            
            if (!validateOfField(choiceOneTxtf, "First Choice")) {
                return;
            }
            
            if (!validateOfField(choiceTwoTxtf, "Second Choice")) {
                return;
            }
            
            if (!validateOfField(choiceThreeTxtf, "Third Choice")) {
                return;
            }
            
            if (!validateOfField(choiceFourTxtf, "Fourth Choice")) {
                return;
            }

            /*Validate the correct answer --> should be integer [0-3] */
            if (!validationOfNumber(answerText, "Question Correct Answer")) {
                
                return;
                
            }
            
            int correctAnswerIndex = Integer.parseInt(answerText.getText());
            
            if (correctAnswerIndex < 0 || correctAnswerIndex > 3) {
                JOptionPane.showMessageDialog(this, "Answer for MCQ should be a number >=0 and < 3", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        }
        
        if (tf.isSelected()) {
            
            if (!(answerText.getText().trim().equals("true") || answerText.getText().trim().equals("false"))) {
                JOptionPane.showMessageDialog(this, "Answer should be 'true' or 'false' only", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        }

        /* All fields are validated now , so get values */
        String qText = qtext.getText().trim();
        String qID = idtext.getText().trim();
        double grade = Double.parseDouble(gradestext.getText().trim());
        
        boolean Added;
        
        if (mcQ.isSelected()) {
            
            String[] choices = {
                choiceOneTxtf.getText().trim(),
                choiceTwoTxtf.getText().trim(),
                choiceThreeTxtf.getText().trim(),
                choiceFourTxtf.getText().trim()};
            int correctAnswerIndex = Integer.parseInt(answerText.getText());
            
            Added = examManager.addNewQuestion(new MCQ(qText, qID, grade, correctAnswerIndex, choices));
            
        } else if (tf.isSelected()) {
            
            boolean answer = Boolean.parseBoolean(answerText.getText().trim());
            Added = examManager.addNewQuestion(new TrueFalseQ(qText, qID, grade, answer));
            
        } else {
            System.out.println("random");
            Added = examManager.addNewQuestion(new FillBlankQ(qText, qID, grade, answerText.getText().trim()));
            System.out.println(Added);
        }
        
        if (Added) {
            JOptionPane.showMessageDialog(this, "New question has been added successfully !", "Add Question", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Addition Wasn't Succesful!.", "Add Question", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jToggleButton1ActionPerformed

