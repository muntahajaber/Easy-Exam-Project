    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (!validateOfField(header, "Exam Header")) {
            return;
        }
        
        if (!validateOfField(nOq, "Number of Questions")) {
            return;
        }
        
        if (!validationOfNumber(nOq, "Number of Questions")) {
            return;
        }
        
        int numOfQuestions;
        try {
            numOfQuestions = Integer.parseInt(nOq.getText().trim());
            
        } catch (NumberFormatException ex) {
            
            JOptionPane.showMessageDialog(this, "Only Integers are allowed in Number of Questions field !", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        /*Chapter name possaible to have empty value (random questions) */
        boolean withAnswer = withAnswers.isSelected();
        
        try {
            
            examManager.generateExam(header.getText(), numOfQuestions, chName.getText().trim(), withAnswer);

            /*Read the generated exam */
            File file = new File(course + "Exam.txt");
            
            BufferedReader br = new BufferedReader(new FileReader(file));
            jTextArea3.setText("");/*clear the text-area*/
            
            String st;
            while ((st = br.readLine()) != null) {
         jTextArea3.append(st+ "\n");
         
                
            }
            
            JOptionPane.showMessageDialog(this, "Exam has been generated successfully !", "Generate Exam", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            
        }        
        
    }//GEN-LAST:event_jButton1ActionPerformed

