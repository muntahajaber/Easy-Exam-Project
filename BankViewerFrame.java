     private boolean validateOfField(JTextField fields, String fieldName) {// changed one
        
        if (fields.getText().trim().equals("")) {

            //display an error message
            JOptionPane.showMessageDialog(this, fieldName + " field is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private boolean validationOfNumber(JTextField field, String fieldName) {//changed one
        
        String st = field.getText().trim();

        //  check if it's invalid number
        try {
            
            Double.parseDouble(st);//if it's not a valid number will throw an exception
            return true; //ok 
        } catch (NumberFormatException e) {

            //error message
            JOptionPane.showMessageDialog(this, "Only Numbers are alowed in " + fieldName + " Field.", "Error", JOptionPane.ERROR_MESSAGE);
            
            return false;
        }
        
    }
